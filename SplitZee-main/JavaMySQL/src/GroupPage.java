    void write_balance() {
        try {
            File file = new File(homePage.grp_id + "_balance.txt");
            FileWriter fw = new FileWriter(file);
            PrintWriter pw = new PrintWriter(fw);
            for (int i = 0; i < homePage.count; i++) {
                for (int j = 0; j < homePage.count; j++) {
                    pw.print(homePage.balance[i][j] + " ");
                }
                pw.println();
            }
            pw.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }


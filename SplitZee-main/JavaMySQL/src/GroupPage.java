    public void showTableData() {
        eventRecord.getColumnModel().getColumn(0).setHeaderValue("Name");
        eventRecord.getColumnModel().getColumn(1).setHeaderValue("Date");
        eventRecord.getColumnModel().getColumn(2).setHeaderValue("Amount");
        eventRecord.getColumnModel().getColumn(3).setHeaderValue("Payee");
        DefaultTableModel model = (DefaultTableModel) eventRecord.getModel();

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/splitzee", "root", "");
            String sql = "select e_name, date, amount, p_name from person, events, occur where g_id=? and occur.e_id=events.e_id and p_id=payee";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, homePage.grp_id);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                model.insertRow(0, new Object[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4)});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }


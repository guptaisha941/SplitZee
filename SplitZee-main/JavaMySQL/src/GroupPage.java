    void print_balance() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/splitzee", "root", "");
            String sql = "select person.p_id, p_name from person, member where g_id=? and member.p_id=person.p_id";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, homePage.grp_id);
            //pst.setString(2, LoginForm.user);
            ResultSet rs = pst.executeQuery();

            textBox.setText("");
            boolean hasOwes = false;
            List<String> members = new ArrayList();
            int count=0;
            while (rs.next()) {
                members.add(rs.getString(2));
                if(rs.getString(1).equals(LoginForm.user)) user_index=count;
                count++;
            }
            for(int i=0; i<count; i++) {
                if(i==user_index) continue;
                float b= homePage.balance[user_index][i];
//                float b = homePage.balance[user_index][i] - homePage.balance[i][user_index];
                String status = "You " + (b < 0 ? "owe " : "lent ");
                if (b < 0) {
                    b *= -1;
                }

                DecimalFormat df = new DecimalFormat("0.00");
                if (b != 0) {
                    hasOwes = true;
                    //textBox.append(status + rs.getString(2) + " Rs. " + df.format(b) + "\n");
                    textBox.append(status + members.get(i) + " Rs. " + df.format(b) + "\n");
                }
            }
            if (!hasOwes) {
                textBox.setText("Your all accounts are settled");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

